
#define __ELIBC_SOURCE
#include <math.h>

#ifdef _HAS_FPU

/*------------------------------------------------------------------------------
// Name: __elibc_fabsf
//----------------------------------------------------------------------------*/
static float __elibc_fabsf(float x) {
	return x > 0 ? x : -x;
}

/*------------------------------------------------------------------------------
// Name: fabsf
//----------------------------------------------------------------------------*/
float fabsf(float x) {
	return __elibc_fabsf(x);
}

#endif
